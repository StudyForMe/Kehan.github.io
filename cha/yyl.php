   <h3>农历公历与阴历阳历</h3>
<p>　　历法小知识：国际通用的历法是以一个太阳回归年为周期，谓之太阳历，简称为阳历，全球公用，故亦称为公历。<font color="#ff0000">阳历即是公历</font>。</p>
<p><img src="https://i.buyiju.com/img/lanmu/yyl.jpg" class="fright p10" width="200" alt="公历农历转换" title="公历农历转换">　　中国传统的历法自夏朝始，称为夏历，是以月球绕地球一周为一月，十二月为一年，月亮号为太阴，故谓之太阴历，简称为阴历。由于阴历的一年只有354天，少于太阳回归年的365天，所以夏历采用置闰月的方法，使得阴历的年份与阳历的年份大概保持一致。所以夏历将太阴历配合了闰年及二十四节气，使得太阴历的年周期同步于太阳历。中国夏历其实是阴阳合历。</p>
<p>　　中国以农业立国，夏历的节气时令等与农业生产密切相关，民国建立后，通用阳历，而将传统的夏历称为农历。一般意义上讲，<font color="#ff0000">阴历即是农历</font>。</p>

